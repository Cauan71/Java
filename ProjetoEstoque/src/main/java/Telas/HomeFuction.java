    public void readJTableForNome(String Nome){
    
        DefaultTableModel modelo = (DefaultTableModel) table.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();
        
        
        for (Produtos p : pdao.readForDesc(Nome)){
            
            modelo.addRow(new Object[]{
            p.getNome(),
            p.getFornecedor(),
            p.getQuantidade(),
            p.getPreco(),
            p.getResponsavel()
            });
                    
        }   
    }
    
