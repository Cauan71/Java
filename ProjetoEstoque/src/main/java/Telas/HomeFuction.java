    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        
        if (table.getSelectedRow() != -1) {
            
            table.setValueAt(txtNomeProduto.getText(), table.getSelectedRow(),0);
            table.setValueAt(txtFornecedorProduto.getText(), table.getSelectedRow(),1);
            table.setValueAt(txtQuantidade.getValue(), table.getSelectedRow(),2);
            table.setValueAt(txtPrecoProduto.getText(), table.getSelectedRow(),3);
            table.setValueAt(txtResponsavel.getText(), table.getSelectedRow(),4);
            
        }
        
        JOptionPane.showMessageDialog(null,"Produto Atualizado com Sucesso!!");
    }//GEN-LAST:event_btnEditarActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        
        if (table.getSelectedRow() != -1) {
            
            txtNomeProduto.setText(table.getValueAt(table.getSelectedRow(), 0).toString());
            txtFornecedorProduto.setText(table.getValueAt(table.getSelectedRow(), 1).toString());
            txtQuantidade.setValue(table.getValueAt(table.getSelectedRow(), 2));
            txtPrecoProduto.setText(table.getValueAt(table.getSelectedRow(), 3).toString());
            txtResponsavel.setText(table.getValueAt(table.getSelectedRow(), 4).toString());
            
            
        }
    }//GEN-LAST:event_tableMouseClicked

